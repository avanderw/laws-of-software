<h1>Laws of Software Engineering</h1>

<div class="laws-container">
    <article class="law">
        <div class="law-title">Atwood's Law</div>
        <div class="law-content">
            Any application that can be written in JavaScript, will eventually
            be written in JavaScript.
        </div>
        <div class="law-author">-- Jeff Atwood, 2009</div>
    </article>

    <article class="law">
        <div class="law-title">Brooks's Law</div>
        <div class="law-content">
            Adding [human resources] to a late software project makes it later.
        </div>
        <div class="law-author">-- Fred Brooks, 1975</div>
    </article>

    <article class="law">
        <div class="law-title">Choose Boring Technology</div>
        <div class="law-content">
            Consider how you would solve your immediate problem without adding
            anything new.
        </div>
        <div class="law-author">-- Dan McKinley, 2015</div>
    </article>

    <article class="law">
        <div class="law-title">Conway's Law</div>
        <div class="law-content">
            Any organization that designs a system (defined broadly) will
            produce a design whose structure is a copy of the organization's
            communication structure.
        </div>
        <div class="law-author">-- Melvin Conway, 1968</div>
    </article>

    <article class="law">
        <div class="law-title">Cunningham's Law</div>
        <div class="law-content">
            The best way to get the right answer on the internet is not to ask a
            question; it's to post the wrong answer.
        </div>
        <div class="law-author">-- Ward Cunningham, 1980</div>
    </article>

    <article class="law">
        <div class="law-title">Doerr's Law</div>
        <div class="law-content">
            We need teams of missionaries, not teams of mercenaries.
        </div>
        <div class="law-author">-- John Doerr, 2015</div>
    </article>

    <article class="law">
        <div class="law-title">Fitt's Law</div>
        <div class="law-content">
            The time to acquire a target is a function of the distance to and
            the size of the target.
        </div>
        <div class="law-author">-- Paul Fitts, 1954</div>
    </article>

    <article class="law">
        <div class="law-title">Gall's Law</div>
        <div class="law-content">
            A complex system that works has evolved from a simple system that
            worked. A complex system built from scratch won't work.
        </div>
        <div class="law-author">-- John Gall, 1975</div>
    </article>

    <article class="law">
        <div class="law-title">Goodhart's Law</div>
        <div class="law-content">
            When a measure becomes a target, it ceases to be a good measure.
        </div>
        <div class="law-author">-- Charles Goodhart, 1975</div>
    </article>

    <article class="law">
        <div class="law-title">Greenspun's tenth rule</div>
        <div class="law-content">
            Any sufficiently complicated C or Fortran program contains an ad
            hoc, informally-specified, bug-ridden, slow implementation of half
            of Common Lisp.
        </div>
        <div class="law-author">-- Philip Greenspun</div>
    </article>

    <article class="law">
        <div class="law-title">Hofstadter's Law</div>
        <div class="law-content">
            It always takes longer than you expect, even when you take into
            account Hofstadter's Law.
        </div>
        <div class="law-author">-- Douglas Hofstadter, 1979</div>
    </article>

    <article class="law">
        <div class="law-title">Hyrum's Law</div>
        <div class="law-content">
            With a sufficient number of users of an API, it does not matter what
            you promise in the contract: all observable behaviors of your system
            will be depended on by somebody.
        </div>
        <div class="law-author">-- Hyrum Wright, 2012</div>
    </article>

    <article class="law">
        <div class="law-title">Kerchkhoff's principle</div>
        <div class="law-content">
            In cryptography, a system should be secure even if everything about
            the system, except for a small piece of information - the key - is
            public knowledge.
        </div>
        <div class="law-author">-- Auguste Kerckhoffs, 1883</div>
    </article>

    <article class="law">
        <div class="law-title">Kernighan's Law</div>
        <div class="law-content">
            Everyone knows that debugging is twice as hard as writing a program
            in the first place. So if you're as clever as you can be when you
            write it, how will you ever debug it?
        </div>
        <div class="law-author">-- Brian Kernighan, 1974</div>
    </article>

    <article class="law">
        <div class="law-title">Knuth's optimization principle</div>
        <div class="law-content">
            Premature optimization is the root of all evil.
        </div>
        <div class="law-author">-- Donald Knuth, 1974</div>
    </article>

    <article class="law">
        <div class="law-title">Law of Leaky Abstractions</div>
        <div class="law-content">
            All non-trivial abstractions, to some degree, are leaky.
        </div>
        <div class="law-author">-- Joel Spolsky, 2002</div>
    </article>

    <article class="law">
        <div class="law-title">Linus's Law</div>
        <div class="law-content">
            Given enough eyeballs, all bugs are shallow.
        </div>
        <div class="law-author">-- Linus Trovalds, 1999</div>
    </article>

    <article class="law">
        <div class="law-title">Lady Lovelace's Objection</div>
        <div class="law-content">
            The Analytical Engine has no pretensions to originate anything. It
            can do whatever we know how to order it to perform.
        </div>
        <div class="law-author">-- Ada Lovelace, 1842</div>
    </article>

    <article class="law">
        <div class="law-title">Moore's Law</div>
        <div class="law-content">
            The complexity for minimum component costs has increased at a rate
            of roughly a factor of two per year. Certainly over the short term
            this rate can be expected to continue, if not to increase. Over the
            longer term, the rate of increase is a bit more uncertain, although
            there is no reason to believe it will not remain nearly constant for
            at least 10 years.
        </div>
        <div class="law-author">-- Gordon Moore, 1965</div>
    </article>

    <article class="law">
        <div class="law-title">Norvig's Law</div>
        <div class="law-content">
            Any technology that surpasses 50% penetration will never double
            again.
        </div>
        <div class="law-author">-- Peter Norvig, 1999</div>
    </article>

    <article class="law">
        <div class="law-title">Parkinson's Law</div>
        <div class="law-content">
            Work expands so as to fill the time available for its completion.
        </div>
        <div class="law-author">-- Cyril Parkinson, 1955</div>
    </article>

    <article class="law">
        <div class="law-title">Peter Principle</div>
        <div class="law-content">
            People in a hierarchy tend to rise to "a level of respective
            incompetence."
        </div>
        <div class="law-author">-- Laurence J. Peter, 1969</div>
    </article>

    <article class="law">
        <div class="law-title">Postel's Law</div>
        <div class="law-content">
            Be conservative in what you send, liberal in what you accept.
        </div>
        <div class="law-author">-- Jon Postel, 1980</div>
    </article>

    <article class="law">
        <div class="law-title">Shirky principle</div>
        <div class="law-content">
            Institutions will try to preserve the problem to which they are the
            solution.
        </div>
        <div class="law-author">-- Clay Shirky, 2010</div>
    </article>

    <article class="law">
        <div class="law-title">Wirth's Law</div>
        <div class="law-content">
            Software gets slower faster than hardware gets faster.
        </div>
        <div class="law-author">-- Niklaus Wirth, 1995</div>
    </article>

    <article class="law">
        <div class="law-title">Zawinski's Law</div>
        <div class="law-content">
            Every program attempts to expand until it can read mail. Those
            programs which cannot so expand are replaced by ones which can.
        </div>
        <div class="law-author">-- James Zawinski, 1995</div>
    </article>
</div>

